#evaluar bool o booleanos - True or False
#and  or   not

a=True
b=False

print(a and b)
#responde False porque no se cumple la condicion de ambos

print(a or b)
#responde True porque una condicion de alguno se cumple

print(not a)
#False porque se le dice no True

print(not(a or b))
#False

print(not (a and b))
#True

